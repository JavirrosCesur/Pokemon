package pokemon;

public class Pokemon {
     
    private String nombre;
    private String mote;

    public String getMote() {
        return mote;
    }
    public void setMote(String mote) {
        this.mote = mote;
    }

    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
